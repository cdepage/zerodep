export type IStreets = keyof typeof addressListStreets;

export const getStreetAbbr = (street: string): string | undefined => {
  const ucStreet = street.toUpperCase();

  if (addressListStreets[ucStreet]) {
    return ucStreet;
  }

  for (const abbr of Object.keys(addressListStreets)) {
    if (addressListStreets[abbr].includes(ucStreet)) {
      return abbr;
    }
  }

  return undefined;
};
